async def main() -> None:
    # Начинаем с понедельника текущей недели относительно 04.07.2026 (то есть с 29.06.2026),
    # чтобы захватить "сегодня" (04.07.2026) и "завтра" (05.07.2026),
    # и заполняем на 4 недели (28 дней), чтобы покрыть и следующие 3 полные недели.
    today = datetime.date(2026, 7, 4)
    start = today - datetime.timedelta(days=today.weekday())
    days = 28  # 4 недели (текущая + 3 будущих)

    rows: list[tuple] = []
    for offset in range(days):
        d = start + datetime.timedelta(days=offset)
        wd = d.weekday()
        if wd not in WEEK_TEMPLATE:
            continue
        for lesson in WEEK_TEMPLATE[wd]:
            num, start_t, end_t, subj, teacher, room, bld, ltype, subgrp = lesson
            rows.append((
                WEEKDAYS_RU[wd],
                d.isoformat(),
                num,
                start_t,
                end_t,
                subj,
                teacher,
                room,
                bld,
                ltype,
                GROUP,
                subgrp,
            ))

    async with aiosqlite.connect(DB_PATH) as conn:
        # Сначала очистим старое расписание для этой группы на указанный период
        end_date = start + datetime.timedelta(days=days - 1)
        await conn.execute(
            """
            DELETE FROM schedule
            WHERE group_name = ? AND date >= ? AND date <= ?
            """,
            (GROUP, start.isoformat(), end_date.isoformat()),
        )
        
        await conn.executemany(
            """
            INSERT INTO schedule
                (day_of_week, date, lesson_number, start_time, end_time,
                 subject, teacher, room, building, lesson_type,
                 group_name, subgroup_name)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
            """,
            rows,
        )
        await conn.commit()
        print(f"Вставлено {len(rows)} записей для группы {GROUP}")


if __name__ == "__main__":
    asyncio.run(main())
